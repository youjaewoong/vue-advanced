<template>
  <div> 
    <div v-for="user in users"> {{ user.title }}</div>
  </div>
</template>

<script>
import axios from 'axios';

export default {
  data() {
    return {
      users: []
    }
  },
  created() {
    axios.get('https://api.hnpwa.com/v0/news/1.json')
    .then(response => {
      console.log(response);
      this.users = response.data;
    })
    .catch(e =>{ 
      console.log(e); 
    })
  }
}
</script>

